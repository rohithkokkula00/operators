public class OperatorsExample {
    public static void main(String[] args) {
        int a = 10, b = 20;

        // Arithmetic
        System.out.println("Addition: " + (a + b));
        System.out.println("Subtraction: " + (a - b));

        // Relational
        System.out.println("a < b: " + (a < b));

        // Logical
        System.out.println("a < b && b > 15: " + (a < b && b > 15));
    }
}
